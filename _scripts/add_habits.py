def format_habits_block(habits_list):
    lines = ["    habits: ["]
    for actor, actions in habits_list:
        lines.append("      { actor: \"" + actor + "\", actions: [")
        for a in actions:
            lines.append("        \"" + a + "\",")
        lines.append("      ]},")
    lines.append("    ]")
    return "\n".join(lines)


def add_habits_to_data():
    content = DATA.read_text(encoding="utf-8")
    updated = 0

    for issue_id, habits in HABITS.items():
        # Find the ISSUE block
        pattern = r'(id: "' + re.escape(issue_id) + r'",[\s\S]+?)(\n    \]\n  \},)'
        m = re.search(pattern, content)
        if not m:
            print(f"  SKIP {issue_id}: pattern not found")
            continue
        block_body = m.group(1)
        # Check if already has habits
        if "habits: [" in block_body:
            print(f"  SKIP {issue_id}: already has habits")
            continue
        # Replace closing
        habits_text = format_habits_block(habits)
        # IMPORTANT: keep research array closing `]` (was lost in original version)
        new_block = block_body + "\n    ],\n" + habits_text + "\n  },"
        content = content[:m.start()] + new_block + content[m.end():]
        updated += 1
        print(f"  OK {issue_id}")

    DATA.write_text(content, encoding="utf-8")
    print(f"\nTotal updated: {updated}")


if __name__ == "__main__":
    add_habits_to_data()
